package com.api.Utils;

import java.io.IOException;
import java.io.InputStream;
import java.util.Arrays;
import java.util.Iterator;
import java.util.List;

import com.api.request.model.userCredentials;
import com.fasterxml.jackson.core.exc.StreamReadException;
import com.fasterxml.jackson.databind.DatabindException;
import com.fasterxml.jackson.databind.ObjectMapper;

public class JsonReaderUtil {

	public static <T> Iterator<T> loadJSON(String fileName,Class<T[]>claszz) {
		
		InputStream is = Thread.currentThread().getContextClassLoader().getResourceAsStream(fileName);
		
		//deserlization : converting json object into java object
		//jackson data bind library  >>>>objectMappper
		
		ObjectMapper objectMapper = new ObjectMapper();
		
		T[] classArray;
		List<T> list = null;
		try {
			classArray = objectMapper.readValue(is, claszz);
			list = Arrays.asList(classArray);
		} catch (IOException e) {
		
			e.printStackTrace();
		}
	
		return list.iterator();
		
	}

}
